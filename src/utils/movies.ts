export const getData = async (offset: number = 0, limit: number = 50) => {
  const duplicatedArray = JSON.parse(JSON.stringify(watched)) as typeof watched;
  const response = duplicatedArray.slice(offset, offset + limit);

  const data = (await Promise.all(
    response.map(async (movie) => {
      const r = await (
        await fetch(
          `https://api.themoviedb.org/3/search/movie?api_key=${apiKey}&language=en-US&query=${movie.Name}&year=${movie.Year}&include_adult=false`
        )
      ).json();
      return r.results[0];
    })
  )).filter(Boolean) as IMovie[];

  return {
    data,
    total: data.length,
    next: offset + limit < duplicatedArray.length ? offset + limit : null,
  };
};
